#!/usr/bin/env python3
"""Generate typed Python modules and documentation from schemas/ontology.yaml.

Usage:
    python scripts/generate_from_ontology.py          # generate files
    python scripts/generate_from_ontology.py --check   # verify files are up-to-date
"""

from __future__ import annotations

import argparse
import difflib
import sys
import textwrap
from pathlib import Path
from typing import Any

import yaml

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
ROOT = Path(__file__).resolve().parent.parent
ONTOLOGY_PATH = ROOT / "schemas" / "ontology.yaml"
TYPES_PATH = ROOT / "cmis_v2" / "generated" / "types.py"
VALIDATORS_PATH = ROOT / "cmis_v2" / "generated" / "validators.py"
DOCS_PATH = ROOT / "docs" / "ontology-map.md"

HEADER = (
    '# Auto-generated from schemas/ontology.yaml by scripts/generate_from_ontology.py\n'
    '# DO NOT EDIT THIS FILE DIRECTLY. Regenerate with:\n'
    '#   python scripts/generate_from_ontology.py\n'
)


def load_ontology() -> dict[str, Any]:
    """Load and return the ontology dict from YAML."""
    with open(ONTOLOGY_PATH, encoding="utf-8") as f:
        raw: dict[str, Any] = yaml.safe_load(f)
    ont: dict[str, Any] = raw["ontology"]
    return ont


# ---------------------------------------------------------------------------
# types.py generation
# ---------------------------------------------------------------------------

def _generate_types(ont: dict[str, Any]) -> str:
    """Return the full source of cmis_v2/generated/types.py."""
    lines: list[str] = []
    lines.append(HEADER)
    lines.append("from __future__ import annotations\n")
    lines.append("from typing import Any, Literal\n")
    lines.append("from pydantic import BaseModel, field_validator\n")
    lines.append("")

    # -- ONTOLOGY_VERSION --
    version = ont.get("version", "0.0.0")
    lines.append(f'ONTOLOGY_VERSION: str = "{version}"')
    lines.append("")

    # -- NodeType Literal --
    node_names = list(ont["node_types"].keys())
    node_literal = ", ".join(f'"{n}"' for n in node_names)
    lines.append(f"NodeType = Literal[{node_literal}]")
    lines.append("")

    # -- EdgeType Literal --
    edge_names = list(ont["edge_types"].keys())
    edge_literal = ", ".join(f'"{e}"' for e in edge_names)
    lines.append(f"EdgeType = Literal[{edge_literal}]")
    lines.append("")

    # -- Trait enum Literals --
    for trait_name, trait_def in ont["trait_definitions"].items():
        if trait_def["type"] == "enum":
            values = trait_def["values"]
            val_literal = ", ".join(f'"{v}"' for v in values)
            type_alias = "".join(part.capitalize() for part in trait_name.split("_")) + "Trait"
            lines.append(f"{type_alias} = Literal[{val_literal}]")
    lines.append("")

    # -- MetricId Literal --
    metric_ids = list(ont["metrics"].keys())
    metric_literal = ", ".join(f'"{m}"' for m in metric_ids)
    lines.append(f"MetricId = Literal[{metric_literal}]")
    lines.append("")

    # -- PolicyMode Literal --
    modes = ont["policy_schema"]["modes"]
    mode_literal = ", ".join(f'"{m}"' for m in modes)
    lines.append(f"PolicyMode = Literal[{mode_literal}]")
    lines.append("")

    # -- WorkflowId Literal --
    workflows = ont["workflow_schema"]["canonical_workflows"]
    wf_literal = ", ".join(f'"{w}"' for w in workflows)
    lines.append(f"WorkflowId = Literal[{wf_literal}]")
    lines.append("")

    # -- Per-node REQUIRED_TRAITS / OPTIONAL_TRAITS --
    lines.append("# Per-node-type trait sets")
    for ntype, ndef in ont["node_types"].items():
        upper = ntype.upper()
        req = ndef.get("required_traits", [])
        opt = ndef.get("optional_traits", [])
        req_str = ", ".join(f'"{t}"' for t in req)
        opt_str = ", ".join(f'"{t}"' for t in opt)
        lines.append(f'{upper}_REQUIRED_TRAITS: frozenset[str] = frozenset({{{req_str}}})')
        lines.append(f'{upper}_OPTIONAL_TRAITS: frozenset[str] = frozenset({{{opt_str}}})')
    lines.append("")

    # -- _REQUIRED_TRAITS / _ALL_TRAITS dicts --
    lines.append("_REQUIRED_TRAITS: dict[str, frozenset[str]] = {")
    for ntype in ont["node_types"]:
        upper = ntype.upper()
        lines.append(f'    "{ntype}": {upper}_REQUIRED_TRAITS,')
    lines.append("}")
    lines.append("")

    lines.append("_ALL_TRAITS: dict[str, frozenset[str]] = {")
    for ntype in ont["node_types"]:
        upper = ntype.upper()
        lines.append(f'    "{ntype}": {upper}_REQUIRED_TRAITS | {upper}_OPTIONAL_TRAITS,')
    lines.append("}")
    lines.append("")

    # -- VALID_METRIC_IDS --
    valid_ids = ", ".join(f'"{m}"' for m in metric_ids)
    lines.append(f"VALID_METRIC_IDS: frozenset[str] = frozenset({{{valid_ids}}})")
    lines.append("")

    # -- METRIC_REGISTRY --
    # Standard fields are always included; all other fields pass through
    # automatically so that new ontology fields don't require codegen changes.
    _STANDARD_FIELDS = {"description", "unit", "aggregation"}
    lines.append("METRIC_REGISTRY: dict[str, dict[str, Any]] = {")
    for mid, mdef in ont["metrics"].items():
        entry_parts = [
            f'"description": "{mdef["description"]}"',
            f'"unit": "{mdef["unit"]}"',
            f'"aggregation": "{mdef["aggregation"]}"',
        ]
        for key, val in mdef.items():
            if key in _STANDARD_FIELDS:
                continue
            if isinstance(val, str):
                entry_parts.append(f'"{key}": "{val}"')
            else:
                entry_parts.append(f'"{key}": {val}')
        lines.append(f'    "{mid}": {{{", ".join(entry_parts)}}},')
    lines.append("}")
    lines.append("")

    # -- OntologyNode --
    lines.append("")
    lines.append("class OntologyNode(BaseModel):")
    lines.append('    """A node in the CMIS ontology graph."""')
    lines.append("")
    lines.append("    node_id: str")
    lines.append("    node_type: NodeType")
    lines.append("    traits: dict[str, Any] = {}")
    lines.append("")
    lines.append('    @field_validator("traits")')
    lines.append("    @classmethod")
    lines.append("    def check_required_traits(cls, v: dict[str, Any], info: Any) -> dict[str, Any]:")
    lines.append('        node_type: str | None = info.data.get("node_type")')
    lines.append("        if node_type is None:")
    lines.append("            return v")
    lines.append("        required = _REQUIRED_TRAITS.get(node_type, frozenset())")
    lines.append("        missing = required - set(v.keys())")
    lines.append("        if missing:")
    lines.append("            raise ValueError(")
    lines.append('                f"Node type {node_type!r} missing required traits: {sorted(missing)}"')
    lines.append("            )")
    lines.append("        return v")
    lines.append("")

    # -- OntologyEdge --
    lines.append("")
    lines.append("class OntologyEdge(BaseModel):")
    lines.append('    """An edge in the CMIS ontology graph."""')
    lines.append("")
    lines.append("    edge_id: str")
    lines.append("    edge_type: EdgeType")
    lines.append("    source_node_id: str")
    lines.append("    target_node_id: str")
    lines.append("    properties: dict[str, Any] = {}")
    lines.append("")

    return "\n".join(lines) + "\n"


