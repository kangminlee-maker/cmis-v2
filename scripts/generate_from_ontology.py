def _generate_docs(ont: dict[str, Any]) -> str:
    """Return the full Markdown content for docs/ontology-map.md."""
    lines: list[str] = []
    lines.append("<!-- Auto-generated from schemas/ontology.yaml by scripts/generate_from_ontology.py -->")
    lines.append("<!-- DO NOT EDIT THIS FILE DIRECTLY. Regenerate with: -->")
    lines.append("<!--   python scripts/generate_from_ontology.py -->")
    lines.append("")
    lines.append(f"# CMIS v2 Ontology Map (v{ont['version']})")
    lines.append("")

    # Node Types
    lines.append("## Node Types")
    lines.append("")
    lines.append("| Name | Description | Required Traits | Optional Traits |")
    lines.append("|------|-------------|-----------------|-----------------|")
    for nname, ndef in ont["node_types"].items():
        req = ", ".join(ndef.get("required_traits", [])) or "-"
        opt = ", ".join(ndef.get("optional_traits", [])) or "-"
        lines.append(f"| `{nname}` | {ndef['description']} | {req} | {opt} |")
    lines.append("")

    # Edge Types
    lines.append("## Edge Types")
    lines.append("")
    lines.append("| Name | Description | Source | Target | Via |")
    lines.append("|------|-------------|--------|--------|-----|")
    for ename, edef in ont["edge_types"].items():
        via = edef.get("via", "-") or "-"
        lines.append(f"| `{ename}` | {edef['description']} | {edef['source']} | {edef['target']} | {via} |")
    lines.append("")

    # Metrics
    lines.append("## Metrics")
    lines.append("")
    lines.append("| ID | Description | Unit | Aggregation |")
    lines.append("|----|-------------|------|-------------|")
    for mid, mdef in ont["metrics"].items():
        lines.append(f"| `{mid}` | {mdef['description']} | {mdef['unit']} | {mdef['aggregation']} |")
    lines.append("")

    # Trait Definitions
    lines.append("## Trait Definitions")
    lines.append("")
    lines.append("| Name | Type | Description | Used By | Values |")
    lines.append("|------|------|-------------|---------|--------|")
    for tname, tdef in ont["trait_definitions"].items():
        used_by = ", ".join(tdef.get("used_by", []))
        values = ", ".join(tdef.get("values", [])) if tdef["type"] == "enum" else "-"
        lines.append(f"| `{tname}` | {tdef['type']} | {tdef['description']} | {used_by} | {values} |")
    lines.append("")

    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def _write_or_check(path: Path, content: str, *, check: bool) -> bool:
    """Write *content* to *path*, or check if it matches.

    Returns True if the file is up-to-date (or was written successfully).
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    if check:
        if not path.exists():
            print(f"MISSING: {path}")
            return False
        existing = path.read_text(encoding="utf-8")
        if existing == content:
            print(f"OK: {path}")
            return True
        diff = difflib.unified_diff(
            existing.splitlines(keepends=True),
            content.splitlines(keepends=True),
            fromfile=str(path) + " (current)",
            tofile=str(path) + " (expected)",
        )
        sys.stdout.writelines(diff)
        print(f"MISMATCH: {path}")
        return False
    else:
        path.write_text(content, encoding="utf-8")
        print(f"GENERATED: {path}")
        return True


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate code from ontology.yaml")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Check that generated files are up-to-date without writing.",
    )
    args = parser.parse_args()

    ont = load_ontology()

    types_src = _generate_types(ont)
    validators_src = _generate_validators(ont)
    docs_src = _generate_docs(ont)

    results = [
        _write_or_check(TYPES_PATH, types_src, check=args.check),
        _write_or_check(VALIDATORS_PATH, validators_src, check=args.check),
        _write_or_check(DOCS_PATH, docs_src, check=args.check),
    ]

    if not all(results):
        sys.exit(1)


if __name__ == "__main__":
    main()
