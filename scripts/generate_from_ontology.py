def _generate_validators(ont: dict[str, Any]) -> str:
    """Return the full source of cmis_v2/generated/validators.py."""
    lines: list[str] = []
    lines.append(HEADER)
    lines.append("from __future__ import annotations\n")
    lines.append("from typing import Any\n")
    lines.append("from cmis_v2.generated.types import (")
    lines.append("    VALID_METRIC_IDS,")
    lines.append("    _ALL_TRAITS,")
    lines.append("    _REQUIRED_TRAITS,")
    lines.append(")")
    lines.append("")
    lines.append("")

    # -- validate_metric_id --
    lines.append("def validate_metric_id(metric_id: str) -> bool:")
    lines.append('    """Return True if *metric_id* is a known metric."""')
    lines.append("    return metric_id in VALID_METRIC_IDS")
    lines.append("")
    lines.append("")

    # -- validate_node_traits --
    lines.append("def validate_node_traits(node_type: str, traits: dict[str, Any]) -> list[str]:")
    lines.append('    """Validate traits for a given node_type.')
    lines.append("")
    lines.append("    Returns a list of error strings (empty means valid).")
    lines.append('    """')
    lines.append("    errors: list[str] = []")
    lines.append("    required = _REQUIRED_TRAITS.get(node_type)")
    lines.append("    if required is None:")
    lines.append('        errors.append(f"Unknown node_type: {node_type!r}")')
    lines.append("        return errors")
    lines.append("    missing = required - set(traits.keys())")
    lines.append("    if missing:")
    lines.append('        errors.append(f"Missing required traits for {node_type!r}: {sorted(missing)}")')
    lines.append("    allowed = _ALL_TRAITS.get(node_type, frozenset())")
    lines.append("    unknown = set(traits.keys()) - allowed")
    lines.append("    if unknown:")
    lines.append('        errors.append(f"Unknown traits for {node_type!r}: {sorted(unknown)}")')
    lines.append("    return errors")
    lines.append("")
    lines.append("")

    # -- validate_pattern_spec --
    lines.append("def validate_pattern_spec(spec: dict[str, Any]) -> list[str]:")
    lines.append('    """Validate a pattern specification dict.')
    lines.append("")
    lines.append("    Checks trait_constraints and benchmark_metrics references.")
    lines.append("    Returns a list of error strings (empty means valid).")
    lines.append('    """')
    lines.append("    errors: list[str] = []")
    lines.append('    pattern_id: str = spec.get("pattern_id", "<unknown>")')
    lines.append("")
    lines.append("    # Validate trait_constraints reference valid node_types and traits")
    lines.append('    constraints = spec.get("trait_constraints", {})')
    lines.append("    for node_type, constraint in constraints.items():")
    lines.append("        all_traits = _ALL_TRAITS.get(node_type)")
    lines.append("        if all_traits is None:")
    lines.append("            # Allow node types not in ontology (e.g., resource, contract, event)")
    lines.append("            continue")
    lines.append('        for trait_name in constraint.get("required_traits", {}).keys():')
    lines.append("            if trait_name not in all_traits:")
    lines.append("                errors.append(")
    lines.append('                    f"[{pattern_id}] Trait {trait_name!r} not valid for '
                 'node_type {node_type!r}"')
    lines.append("                )")
    lines.append('        for trait_name in constraint.get("optional_traits", {}).keys():')
    lines.append("            if trait_name not in all_traits:")
    lines.append("                errors.append(")
    lines.append('                    f"[{pattern_id}] Trait {trait_name!r} not valid for '
                 'node_type {node_type!r}"')
    lines.append("                )")
    lines.append("")
    lines.append("    # Validate benchmark_metrics reference valid metric IDs")
    lines.append('    for met_id in spec.get("benchmark_metrics", []):')
    lines.append("        if not validate_metric_id(met_id):")
    lines.append("            errors.append(")
    lines.append('                f"[{pattern_id}] Unknown benchmark metric: {met_id!r}"')
    lines.append("            )")
    lines.append("")
    lines.append("    return errors")
    lines.append("")

    return "\n".join(lines) + "\n"


